package model;

public class StarRate {
	private int rate_no;     // 별점 번호
	private int star_rate; // 별점
	private int rno;         // 식당 번호
	private int mno;         // 회원 번호
	
	public int getRate_no() {
		return rate_no;
	}
	public void setRate_no(int rate_no) {
		this.rate_no = rate_no;
	}
	public int getStar_rate() {
		return star_rate;
	}
	public void setStar_rate(int star_rate) {
		this.star_rate = star_rate;
	}
	public int getRno() {
		return rno;
	}
	public void setRno(int rno) {
		this.rno = rno;
	}
	public int getMno() {
		return mno;
	}
	public void setMno(int mno) {
		this.mno = mno;
	}
	
	

}
